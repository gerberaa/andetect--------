def get_random_user_agent():
    """Отримання випадкового User-Agent"""
    import random
    return random.choice(USER_AGENTS)

def get_user_agents_by_browser(browser_name):
    """Отримання User-Agent для конкретного браузера"""
    browser_filters = {
        'chrome': 'Chrome/',
        'firefox': 'Firefox/',
        'safari': 'Safari/',
        'edge': 'Edg/',
        'opera': 'OPR/',
        'vivaldi': 'Vivaldi/',
        'brave': 'Brave/'
    }
    
    if browser_name.lower() in browser_filters:
        filter_str = browser_filters[browser_name.lower()]
        return [ua for ua in USER_AGENTS if filter_str in ua]
    
    return USER_AGENTS

def get_user_agents_by_os(os_name):
    """Отримання User-Agent для конкретної ОС"""
    os_filters = {
        'windows': 'Windows NT',
        'macos': 'Macintosh',
        'linux': 'Linux'
    }
    
    if os_name.lower() in os_filters:
        filter_str = os_filters[os_name.lower()]
        return [ua for ua in USER_AGENTS if filter_str in ua]
    
    return USER_AGENTS

